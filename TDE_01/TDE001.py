# CRIE  um algoritmo que leia um número inteiro e escreva seu antecessor e sucessor

number = int(input('Olá, digite seu número: '))

PredecessorNumber = number - 1
SuccessorNumber = number + 1


print('O número escolhido foi: ', number)
print('o antecessor do seu número é: ', PredecessorNumber)
print('o sucessor do seu número é: ', SuccessorNumber)
